package mechanics;

/**
 * Class PieceType
 *
 * Functionalities: Provides a enumeration of all the possible pieces a Piece object can be.
 */
public enum PieceType {
    /**
     * Pawn
     */
    PAWN,

    /**
     * Rook
     */
    ROOK,

    /**
     * Knight
     */
    KNIGHT,

    /**
     * Bishop
     */
    BISHOP,

    /**
     * King
     */
    KING,

    /**
     * Queen
     */
    QUEEN,

    /**
     * Empty board space - no piece at all
     */
    NONE,

    /**
     * Outside of the board
     */
    OUTSIDE
}

